"""
유사 단어 혼동 — 의미가 다르지만 혼동하기 쉬운 단어 쌍.

단어 치환으로 의미 오류를 생성.
"""

import random
from typing import Optional


# ── 유사 단어 혼동 ──
# (올바른 표현, [잘못 대체되는 표현들])
# 주의: 문맥에 따라 어느 쪽이 '올바른' 것인지 달라질 수 있음.
#       여기서는 가장 흔한 혼동 방향을 기준으로 함.
WORD_CONFUSION: dict[str, list[str]] = {
    # ── 대표적 혼동 쌍 ──
    "다르다": ["틀리다"],
    "다른": ["틀린"],
    "다르게": ["틀리게"],

    "가리키다": ["가르치다"],
    "가리켰다": ["가르쳤다"],

    "낫다": ["낳다"],          # 나은/낫은 의미일 때
    "부치다": ["붙이다"],      # 편지 보내기 의미
    "붙이다": ["부치다"],      # 접착 의미

    "잊다": ["잃다"],
    "잊어버리다": ["잃어버리다"],

    "드러나다": ["들어나다"],   # 밝혀지다 의미
    "드러내다": ["들어내다"],
    "들이다": ["드리다"],

    "좇다": ["쫓다"],          # 추구하다 의미
    "쫓다": ["쫒다"],          # 추적하다

    # ── 얇다/가늘다, 두껍다/굵다 ──
    "가늘다": ["얇다"],        # 굵기에 대해 쓸 때
    "가는": ["얇은"],
    "굵다": ["두껍다"],

    # ── 바라다/바래다 ──
    "바라": ["바래"],          # 희망할 때
    "바랍니다": ["바랩니다"],
    "바라요": ["바래요"],

    # ── 조리다/졸이다 ──
    "조림": ["졸임"],          # 양념 배게 끓인 것
    "졸임": ["조림"],          # 국물 줄인 것

    # ── 들르다/들리다 ──
    "들르다": ["들리다"],      # 잠깐 방문하다
    "들렀다": ["들렸다"],

    # ── 너비/넓이 ──
    "너비": ["넓이"],          # 가로 길이
    "넓이": ["너비"],          # 면적

    # ── 맞다 관련 ──
    "맞히다": ["맞추다"],      # 정답을 맞히다
    "맡다": ["맞다"],          # 맡기다/냄새맡다 의미일 때

    # ── 기타 의미 혼동 ──
    "곪다": ["곯다"],
    "곯다": ["곪다"],
    "건승": ["건성"],
    "결딴나다": ["결단나다"],
    "옥새": ["옥쇄"],
    "유도신문": ["유도심문"],
    "역전": ["역전 앞"],       # 겹말
    "사달이 나다": ["사단이 나다"],

    # ── 결제/결재 ──
    "결제": ["결재"],          # 돈거래 의미
    "결재": ["결제"],          # 서류 승인 의미

    # ── 초초/초조 ──
    "초조하다": ["초초하다"],

    # ── 실제로/실지로 ──
    "실제로": ["실지로"],

    # ── 시각/시간 ──
    "현재 시각": ["현재 시간"],  # 엄밀히는 구별
}


def apply_word_substitution(text: str, rng: random.Random) -> Optional[str]:
    """
    텍스트에서 유사 단어 혼동 오류를 적용.

    Args:
        text: 올바른 한국어 문장
        rng: 랜덤 시드 관리용 Random 인스턴스

    Returns:
        오류가 적용된 문장. 적용 가능한 패턴이 없으면 None.
    """
    candidates: list[tuple[str, str]] = []
    for correct, wrongs in WORD_CONFUSION.items():
        if correct in text:
            for wrong in wrongs:
                candidates.append((correct, wrong))

    if not candidates:
        return None

    correct, wrong = rng.choice(candidates)
    return text.replace(correct, wrong, 1)


def get_error_count() -> int:
    """이 모듈이 가진 오류 패턴 수를 반환."""
    return sum(len(v) for v in WORD_CONFUSION.values())
